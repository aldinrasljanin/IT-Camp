// 1. Napisati f-ju koja vraca kvadrat datih brojeva.
const kvadratbroja = (broj) => {
  return broj * broj;
};
console.log(kvadratbroja(2));
console.log(kvadratbroja(9));
console.log(kvadratbroja(15));

// 2. Napisati f-ju koja izracunava povrsinu datog pravougaonika.
const povrsinaPravougaonika = (a, b) => {
  return a * b;
};
console.log(povrsinaPravougaonika(2, 5));
